# a) Uma função que calcula a média de 3 números inteiros
def MediaA():
    n1 = int(input("Introduza o primeiro numero"))
    n2 = int(input("Introduza o segundo numero"))
    n3 = int(input("Introduza o terceiro numero"))
    
    resultado = 0
    resultado = n1 + n2 + n3
    resultado = resultado / 3
    print(resultado)

MediaA()